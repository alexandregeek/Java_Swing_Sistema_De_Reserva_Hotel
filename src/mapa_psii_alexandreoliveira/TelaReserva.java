    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String TipoSuite = jTextFieldTipoSuite.getText();
        String QtDiarias = jTextFieldQtDiarias.getText();
        String QtPessoas = jTextFieldQtPessoas.getText();
        String QtCrianca = jTextFieldQtCrianca.getText();
        String NomeResponsavel = jTextFieldNomeResponsavel.getText();
        
        //int numeroConvertido = Integer.parseInt(numero);
        int qtDiarias = Integer.parseInt(QtDiarias);
        int qtCriancas = Integer.parseInt(QtCrianca);
        int qtPessoas = Integer.parseInt(QtPessoas); 
       
        if (TipoSuite.equals("Standard")){
            valorDiarias =  130 * qtDiarias;
        }
        
        
       
        if (TipoSuite.equals("Luxo")){
            valorDiarias = 180 * qtDiarias;
        }
        
        
        //calcularValorDiarias();
      
        if(qtDiarias >= 7 ){
        desconto = valorDiarias * 0.1;
        
        }
        
        if(qtDiarias <= 7 ){
         valorPagar = valorDiarias;
        }
                
        valorPagar = valorDiarias - desconto;        
       
        
        
        jLabelValorPagar.setText(""+ valorPagar);
        jLabelDesconto.setText(""+ desconto);
        
        compDisponib = qtPessoas-qtCriancas; 
        
        if( compDisponib >= 4 ){
        jLabelCapasidade.setText("Camas insuficientes" );
        }
        
        if(compDisponib <= 3 ){
        jLabelCapasidade.setText(" Suite com camas disponiveis"  );
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

